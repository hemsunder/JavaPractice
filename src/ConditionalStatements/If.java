package ConditionalStatements;

public class If {

	public static void main(String[] args) {
		
		
		int a = 50;
		
		System.out.println("Test started");
		
		if(a>120){
			System.out.println("1st block execution: a value is "+a);
		}
		
		if(a<110){
			System.out.println("2nd block execution: a value is "+a);
		}

		System.out.println("Test ended");
	}

}
