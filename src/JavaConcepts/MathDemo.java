package JavaConcepts;

public class MathDemo {

	public static void main(String[] args) {
		
		System.out.println(Math.max(150, 240));
		System.out.println(Math.min(150, 240));
		System.out.println(Math.random());
		System.out.println(Math.abs(-1002.35));
		System.out.println(Math.sqrt(1452));

	}

}
